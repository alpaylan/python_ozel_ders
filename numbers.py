
# int -> unbounded
i0 = 0
i1 = int(0)
i2 = int(0.0)  # i2 = 0

# float -> unbounded
f0 = 0.0
f1 = float(0.0)
f2 = float(0)  # f2 = 0.0

# complex -> unbounded
c0 = 1j
c1 = 5 + 2.20j
c2 = complex(1, 3)

print(i0)
print(i1)
print(i2)

print(f0)
print(f1)
print(f2)

print(c0)
print(c1)
